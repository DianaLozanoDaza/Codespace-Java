package ListasEnlazadas;

public class Profesor {
    int codigo;
    String nombre;
    String apelido;
    String celular;

    public Profesor(int codigo, String nombre, String apelido, String celular) {
        this.codigo = codigo;
        this.nombre = nombre;
        this.apelido = apelido;
        this.celular = celular;
    }
}
